	ld bc, #f40e
	out (c),c
	ld bc, #f6c0
	out (c),c
	xor a
	out (c),a
	ld bc, #f792
	out (c),c
	ld bc, #f645
	out (c),c
	ld b, #f4
	in a,(c)
	ld bc, #f782
	out (c),c
	ld bc, #f600
	out (c),c
	ei

	rla
	jp c,prog

; Exit

	ret

